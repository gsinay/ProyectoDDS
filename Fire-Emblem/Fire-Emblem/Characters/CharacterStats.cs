namespace Fire_Emblem.Characters;

public class CharacterStats
{
    public int Atk;
    public int Spd;
    public int Res;
    public int Def;

    public CharacterStats(int Atk, int Spd, int Res, int Def)
    {
        this.Atk = Atk;
        this.Spd = Spd;
        this.Res = Res;
        this.Def = Def;
    }
}